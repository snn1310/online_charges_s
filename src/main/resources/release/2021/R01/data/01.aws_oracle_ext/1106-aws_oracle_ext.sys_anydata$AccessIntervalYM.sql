CREATE OR REPLACE FUNCTION aws_oracle_ext.sys_anydata$accessintervalym(jsonb_value jsonb)
 RETURNS INTERVAL YEAR TO MONTH
IMMUTABLE
STRICT
PARALLEL SAFE
AS
$BODY$
SELECT
  CASE WHEN (aws_oracle_ext.sys_anydata$gettypename($1) = 'SYS.INTERVAL_YEAR_MONTH')
   AND ($1 ->> 'SYS.INTERVAL_YEAR_MONTH' <> '') 
  THEN
	 ($1->> 'SYS.INTERVAL_YEAR_MONTH')::INTERVAL YEAR TO MONTH
  ELSE
     NULL
  END;
$BODY$
LANGUAGE sql;